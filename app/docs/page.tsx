function CopyBtn({ text }: { text: string }) {
  const [copied, setCopied] = useState(false)
  return (
    <button
      onClick={() => { navigator.clipboard.writeText(text); setCopied(true); setTimeout(() => setCopied(false), 2000) }}
      style={{ padding: '4px 10px', fontSize: 9, background: copied ? G : 'transparent', color: copied ? '#000' : G, border: `1px solid ${G}`, cursor: 'pointer', letterSpacing: '0.08em', flexShrink: 0 }}
    >
      {copied ? '✓ COPIED' : 'COPY'}
    </button>
  )
}

