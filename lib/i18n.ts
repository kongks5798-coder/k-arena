export type Locale = 'ko' | 'en' | 'ja' | 'zh' | 'de' | 'fr' | 'es'

export function getLocale(): Locale {
  if (typeof navigator === 'undefined') return 'en'
  const lang = navigator.language.slice(0, 2).toLowerCase()
  const map: Record<string, Locale> = { ko: 'ko', ja: 'ja', zh: 'zh', de: 'de', fr: 'fr', es: 'es' }
  return map[lang] ?? 'en'
}

